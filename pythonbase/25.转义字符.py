# 转义字符 意义
# \a 响铃(BEL)
# \b 退格(BS)，将当前位置移到前⼀列
# \f 换⻚(FF)，将当前位置移到下⻚开头
# \n 换⾏(LF)，将当前位置移到下⼀⾏开头
# \r 回⻋(CR)，将当前位置移到本⾏开头
# \t ⽔平制表(HT) (跳到下⼀个TAB位置)
# \v 垂直制表(VT)
# \\ 代表⼀个反斜杠字符"\"
# \' 代表⼀个单引号(撇号)字符
# \" 代表⼀个双引号字符
# \? 代表⼀个问号
# \0 空字符(NUL)
# \ddd 1到3位⼋进制所代表的任意字符
# \xhh 1到2位⼗六进制所代表的任意字符
# 注意1：区分斜杠“/”和反斜杠“\”，此处不可互换